import pandas as pd

df = pd.read_csv("ai_student_impact_dataset.csv")

df

df.info()

x = df.drop(["Burnout_Risk_Level","Student_ID"], axis=1)
y = df["Burnout_Risk_Level"]
print("X shape: ",x.shape)
print("y Shape",y.shape)

print(x.select_dtypes(include="object").columns.tolist())

print(y.unique())

from sklearn.model_selection import train_test_split
x_train,x_test, y_train,y_test = train_test_split(
    x,
    y,
    test_size=0.2,
    random_state=42

)

print("x train",x_train.shape)
print("x train",x_test.shape)
print("x train",y_train.shape)
print("x train",y_test.shape)

from logging import Handler
from sklearn import preprocessing
from sklearn.compose import ColumnTransformer
from sklearn.preprocessing import OneHotEncoder
cat_cols = x_train.select_dtypes(include="object").columns
preprocessing = ColumnTransformer(
    transformers=[
        ("cat", OneHotEncoder(handle_unknown="ignore"),cat_cols)


    ],
    remainder="passthrough"
)
print(cat_cols)

X_train_encoded = preprocessing.fit_transform(x_train)
X_test_encoded = preprocessing.transform(x_test)

print("Encoded Train Shape:", X_train_encoded.shape)
print("Encoded Test Shape :", X_test_encoded.shape)

from sklearn.ensemble import RandomForestClassifier

model = RandomForestClassifier(
    n_estimators=100,
    random_state=42
)

model.fit(X_train_encoded, y_train)

print("Training Complete!")

predictions = model.predict(X_test_encoded)

print(predictions[:10])

from sklearn.metrics import accuracy_score

accuracy = accuracy_score(y_test, predictions)

print("Accuracy:", accuracy)

print(y.value_counts())

from sklearn.metrics import classification_report

print(classification_report(y_test, predictions))

import joblib

joblib.dump(preprocessing, "preprocessor.pkl")

print("Done")

